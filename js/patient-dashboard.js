// Edit Patient
// =======================
function editPatient(id) {
    const patient = patientsData.find(p => (p.patientId && p.patientId === id) || (p._id && p._id === id));
    if (patient) {
        document.getElementById('editPatientId').value = patient.patientId || patient._id;
        document.getElementById('p_firstName').value = patient.firstName || '';
        document.getElementById('p_lastName').value = patient.lastName || '';
        document.getElementById('p_dob').value = patient.dob ? Utils.formatDate(patient.dob) : '';
        document.getElementById('p_age').value = patient.age || '';
        document.getElementById('p_gender').value = patient.gender || '';
        document.getElementById('p_phone').value = patient.phone || '';
        document.getElementById('p_email').value = patient.email || '';
        document.getElementById('p_city').value = patient.city || '';
        document.getElementById('p_address').value = patient.address || '';
        document.getElementById('p_assignedDoctor').value = patient.assignedDoctor || '';
        document.getElementById('p_registrationDate').value = patient.registrationDate ? patient.registrationDate.split('T')[0] : (patient.createdAt ? patient.createdAt.split('T')[0] : '');
        document.getElementById('patientModalTitle').innerText = 'Edit Patient';
        const subtitle = document.getElementById('patientModalSubtitle');
        if (subtitle) {
            const regDate = patient.registrationDate ? Utils.formatDate(patient.registrationDate) : (patient.createdAt ? Utils.formatDate(patient.createdAt).split('T')[0] : 'N/A');
            subtitle.innerText = `Editing: ${patient.firstName} ${patient.lastName} | Registered: ${regDate}`;
            subtitle.style.display = 'block';
        }
        Modal.open('addPatientModal');
    }
}

// =======================
// Render Patients
// =======================
function setViewMode(mode) {
    currentView = mode;


    // Update container class
    const container = document.getElementById('patientsContainer');
    if (container) {
        container.className = mode === 'list' ? 'list-mode' : 'grid-mode';
    }

    // Highlight active button
    document.getElementById('btn-list-view')?.classList.toggle('active', mode === 'list');
    document.getElementById('btn-grid-view')?.classList.toggle('active', mode === 'grid');

    renderPatients();
}

function renderPatients() {
    const container = document.getElementById('patientsContainer');
    if (!container) return;

    container.innerHTML = '';

    const searchText = document.getElementById('searchPatient')?.value.toLowerCase() || '';
    const searchType = document.getElementById('searchTypePatient')?.value || 'all';

    const filtered = patientsData.filter(p => {
        if (!p) return false;
        const fullName = `${p.firstName || ''} ${p.lastName || ''}`.toLowerCase();

        switch (searchType) {
            case 'id':
                return p.patientId?.toLowerCase().includes(searchText) || p._id?.toLowerCase().includes(searchText);
            case 'name':
                return fullName.includes(searchText);
            case 'phone':
                return (p.phone || '').includes(searchText);
            case 'all':
            default:
                return (p.patientId && p.patientId.toLowerCase().includes(searchText)) ||
                    (p._id && p._id.toLowerCase().includes(searchText)) ||
                    fullName.includes(searchText) ||
                    (p.phone || '').includes(searchText) ||
                    (p.email || '').toLowerCase().includes(searchText);
        }
    });

    filtered.forEach(p => {
        const patientEl = document.createElement('div');
        patientEl.className = currentView === 'list' ? 'patient-list-item card' : 'patient-grid-item card';
        const fullName = `${p.firstName || ''} ${p.lastName || ''}`.trim();

        patientEl.innerHTML = `
            <div class="patient-info">
                <strong>${fullName}</strong>
                <div class="patient-card-reg-date" style="font-size: 0.8rem; color: #777; margin-top: -3px; margin-bottom: 5px;">
                    Registration Date: ${p.registrationDate ? Utils.formatDate(p.registrationDate) : (p.createdAt ? Utils.formatDate(p.createdAt).split('T')[0] : 'N/A')}
                </div>
                <div class="patient-meta">
                    <span class="meta-row"><span class="meta-label">ID:</span> ${Utils.formatPatientId(p.patientId || p._id)} &nbsp;|&nbsp; <span class="meta-label">Age:</span> ${p.age || '-'}</span>
                    <span class="meta-row"><span class="meta-label">Phone:</span> ${p.phone || '-'}</span>
                    <span class="meta-row"><span class="meta-label">City:</span> ${p.city || '-'} &nbsp;|&nbsp; <span class="meta-label">Gender:</span> ${p.gender || '-'}</span>
                </div>
            </div>
            <div class="patient-actions">
                <button class="btn btn-primary" onclick="viewPatient('${p.patientId || p._id}')">View</button>
                <button class="btn btn-secondary" onclick="editPatient('${p.patientId || p._id}')">Edit</button>
                <button class="btn btn-danger" onclick="deletePatient('${p.patientId || p._id}')">Delete</button>
            </div>
        `;
        container.appendChild(patientEl);
    });

    if (filtered.length === 0) {
        container.innerHTML = '<div class="no-results">No patients found</div>';
    }
}

